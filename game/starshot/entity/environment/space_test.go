func TestSeededStarsCoverScreen(t *testing.T) {
	scene := testutil.NewMockScene()

	// High density to ensure coverage
	_ = environment.NewSpace(0.2, scene)

	// Check that stars are distributed across Y range
	minY := scene.Height()
	maxY := 0

	for _, entity := range scene.GetEntities() {
		if entity.Type() != def.EntityTypeBackground {
			continue
		}
		_, y := entity.Location()
		if y < minY {
			minY = y
		}
		if y > maxY {
			maxY = y
		}
	}

	got := maxY - minY
	want := int(float64(scene.Height()-1) * 0.8) // At least 80% coverage

	if got < want {
		t.Errorf("Y range = %d, want >= %d (80%% of screen height)", got, want)
	}

	t.Logf("Seeded stars Y range: %d to %d (range=%d)", minY, maxY, got)
}

func TestStarColorsAreValid(t *testing.T) {
	scene := testutil.NewMockScene()
	space := environment.NewSpace(0.5, scene)

	// Generate many stars
	for i := 0; i < 1000; i++ {
		space.Act(scene)
	}

	invalidCount := 0
	for _, entity := range scene.GetEntities() {
		if entity.Type() != def.EntityTypeBackground {
			continue
		}

		// We can't directly access color from the interface, but we can verify
		// the entity was created successfully and has valid dimensions
		w, h := entity.Dimensions()
		if w <= 0 || h <= 0 {
			invalidCount++
		}
	}

	if invalidCount > 0 {
		t.Errorf("Found %d stars with invalid dimensions", invalidCount)
	}
}
