func TestSpaceImplementsEntity(t *testing.T) {
	scene := testutil.NewMockScene()
	space := environment.NewSpace(0.05, scene)

	var _ def.Entity = space

	// Verify all interface methods work
	if got := space.Type(); got != def.EntityTypeEnvironment {
		t.Errorf("Type() = %v, want %v", got, def.EntityTypeEnvironment)
	}

	x, y := space.Location()
	if x != 0 || y != 0 {
		t.Errorf("Location() = (%d,%d), want (0,0)", x, y)
	}

	w, h := space.Dimensions()
	if w != def.ScreenWidth || h != def.ScreenHeight {
		t.Errorf("Dimensions() = (%d,%d), want (%d,%d)", w, h, def.ScreenWidth, def.ScreenHeight)
	}

	if space.CanBeRemoved() {
		t.Error("Space should never be removed")
	}

	if space.BoundingBoxOverlaps(space) {
		t.Error("Space should not overlap anything")
	}
}

func TestLayerSizeAndSpeed(t *testing.T) {
	testCases := []struct {
		layer     environment.Layer
		wantSize  int
		wantSpeed int
	}{
		{environment.Close, 5, 5},
		{environment.Mid, 3, 3},
		{environment.Far, 2, 1},
	}

	for _, tc := range testCases {
		t.Run(tc.layer.String(), func(t *testing.T) {
			gotSize, gotSpeed := environment.SizeAndSpeedForLayer(tc.layer)

			if gotSize != tc.wantSize {
				t.Errorf("size = %d, want %d", gotSize, tc.wantSize)
			}

			if gotSpeed != tc.wantSpeed {
				t.Errorf("speed = %d, want %d", gotSpeed, tc.wantSpeed)
			}
		})
	}
}

func TestLayerSizeSpeedRelationships(t *testing.T) {
	closeSize, closeSpeed := environment.SizeAndSpeedForLayer(environment.Close)
	midSize, midSpeed := environment.SizeAndSpeedForLayer(environment.Mid)
	farSize, farSpeed := environment.SizeAndSpeedForLayer(environment.Far)

	// Verify size ordering
	if !(closeSize >= midSize && midSize >= farSize) {
		t.Errorf("Size ordering broken: Close=%d, Mid=%d, Far=%d", closeSize, midSize, farSize)
	}

	// Verify speed ordering
	if !(closeSpeed >= midSpeed && midSpeed >= farSpeed) {
		t.Errorf("Speed ordering broken: Close=%d, Mid=%d, Far=%d", closeSpeed, midSpeed, farSpeed)
	}

	// Verify minimum size for visibility
	if farSize < 1 {
		t.Errorf("Far layer size %d is too small (invisible)", farSize)
	}
}

