func TestAllLayersGenerate(t *testing.T) {
	scene := testutil.NewMockScene()
	space := environment.NewSpace(0.5, scene) // High density

	// Generate many stars
	for i := 0; i < 500; i++ {
		space.Act(scene)
	}

	// Count stars by size (proxy for layer)
	closeSize, _ := environment.SizeAndSpeedForLayer(environment.Close)
	midSize, _ := environment.SizeAndSpeedForLayer(environment.Mid)
	farSize, _ := environment.SizeAndSpeedForLayer(environment.Far)

	closeSeen := 0
	midSeen := 0
	farSeen := 0

	for _, entity := range scene.GetEntities() {
		w, _ := entity.Dimensions()
		switch w {
		case closeSize:
			closeSeen++
		case midSize:
			midSeen++
		case farSize:
			farSeen++
		}
	}

	if closeSeen == 0 {
		t.Error("No Close layer stars generated")
	}
	if midSeen == 0 {
		t.Error("No Mid layer stars generated")
	}
	if farSeen == 0 {
		t.Error("No Far layer stars generated")
	}

	t.Logf("Generated: Close=%d, Mid=%d, Far=%d", closeSeen, midSeen, farSeen)
}

func TestNewStarsStartAboveScreen(t *testing.T) {
	scene := testutil.NewMockScene()
	// Use density 0 to avoid seeding, then use high density for generation
	space := environment.NewSpace(0, scene)

	// Manually set density high for Act() calls
	// (We'd need to expose this or use a different approach)
	// For now, create a new space with high density after initial seeding
	scene = testutil.NewMockScene()
	space = environment.NewSpace(0.5, scene)

	// Clear seeded stars to test only Act()-generated stars
	initialCount := scene.EntityCount()

	// Generate new stars via Act()
	for range 100 {
		space.Act(scene)
	}

	// Check only stars added after seeding
	newStarsChecked := 0
	for i := initialCount; i < scene.EntityCount(); i++ {
		entity := scene.GetEntities()[i]
		if entity.Type() != def.EntityTypeBackground {
			continue
		}

		_, y := entity.Location()
		if y >= 0 {
			t.Errorf("Star %d (generated by Act) at y=%d, want < 0 to avoid pop-in", i, y)
		}
		newStarsChecked++
	}

	if newStarsChecked == 0 {
		t.Error("No new stars generated by Act() to test")
	}
}

func TestStarDensityAffectsGeneration(t *testing.T) {
	iterations := 200

	// Low density
	sceneLow := testutil.NewMockScene()
	spaceLow := environment.NewSpace(0.01, sceneLow)
	for i := 0; i < iterations; i++ {
		spaceLow.Act(sceneLow)
	}

	// High density
	sceneHigh := testutil.NewMockScene()
	spaceHigh := environment.NewSpace(0.10, sceneHigh)
	for i := 0; i < iterations; i++ {
		spaceHigh.Act(sceneHigh)
	}

	got := sceneHigh.EntityCount()
	baseline := sceneLow.EntityCount()

	if got <= baseline {
		t.Errorf("high density stars = %d, want > %d (low density)", got, baseline)
	}

	t.Logf("Low density (0.01): %d stars, High density (0.10): %d stars", baseline, got)
}

